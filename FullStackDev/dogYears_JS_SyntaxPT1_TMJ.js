

//Dog years assignment
// With your knowledge of math operators and variables, use JavaScript to convert your human age into dog years.

//var myAge. will be 26 for this implementation
let myAge = 26;

//earlyYears var. Can be changed later
let earlyYears = 2;

//getting proper early dog years through multiplication of 10.5 x number of years, which in this case is 2
earlyYears = earlyYears * 10.5;


//performing math to calculate dog years.

//first subtracting 2 years off of my input age. stored in var laterYears
laterYears = myAge - 2;

//later years in dog years calc
laterYears = laterYears * 4;

//verifying my calculations through print debugging
console.log(`2 Early years = ${earlyYears} dog years\n\n`);

console.log(`Remaining ${myAge - 2} human years should equal ${laterYears} dog years\n\n`);


//dog years variable created to store final dog years value
let myAgeInDogYears = earlyYears + laterYears;

//var my name, in lowercase
var myName = "Timothy";
myName = myName.toLowerCase();

//printing my name and age in dog years
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.\n\n`);

